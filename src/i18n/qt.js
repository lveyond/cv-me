export const t = (key, lang = null) => {
    const currentLang = lang || getCurrentLanguage()
    const keys = key.split('.')
    let value = qtLanguages[currentLang]

    for (const k of keys) {
        if (value && k in value) {
            value = value[k]
        } else {
            return key
        }
    }

    return value
}